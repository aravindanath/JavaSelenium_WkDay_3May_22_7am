package day1;

public class Loops1 {

    public static void main(String[] args) {


        /**
         *  for(dec/inc ;condition;increment/decrement){
         *  CODE
         *  }
         *
         *   i+=2
         *   i=i+2
         */

        for(int i=1;i<=10;i++){
            System.out.println(i + " Arvind");
        }

        System.out.println("*********");

        for(int i=10;i>=1;i--){
            System.out.println(i);
        }


    }
}
